#include "3-calc.h"

/**
 * op_add - add a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtract b from a
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply a by b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide a by b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - a modulo b
 */
int op_mod(int a, int b)
{
	return (a % b);
}

